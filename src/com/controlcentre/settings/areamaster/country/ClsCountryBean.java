package com.controlcentre.settings.areamaster.country;

import java.util.Date;

public class ClsCountryBean {

	
	private String country;
	private String contry_code;
	private String region;
	private int reg_id;
    private Date date_coun;
	private int docno;
    private String mode;
    
    
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getContry_code() {
		return contry_code;
	}
	public void setContry_code(String contry_code) {
		this.contry_code = contry_code;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public int getReg_id() {
		return reg_id;
	}
	public void setReg_id(int reg_id) {
		this.reg_id = reg_id;
	}
	public Date getDate_coun() {
		return date_coun;
	}
	public void setDate_coun(Date date_coun) {
		this.date_coun = date_coun;
	}
	public int getDocno() {
		return docno;
	}
	public void setDocno(int docno) {
		this.docno = docno;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
    
    
	 
}
